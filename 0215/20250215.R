## 조건문, 반복문 : 문법만 알면 가능
# 왜? R 언어로는 깊게 코딩 하지 않음. 기존 정리된 데이터를 시각화에 중점

# 문: 문장을 의미
# 조건문: 조건이 들어간 문장
# 반복문: 반복이 들어간 문장

if(5 < 0){             #만약에 5가 0보다 크다면 '맞습니다' 출력
  print('맞습니다.')    # 조건이 참일때 실행할 코드
}else{
  print('틀렸습니다')   # 조건이 거짓일때 실행할 코드
}


### ***시험필수**** (이정도만 알면 충분)
# ifelse
