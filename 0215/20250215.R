### 05. 벡터 (Vector) / 데이터 분석의 핵심!!!!!!!
# 동일한 ' 데이터 타입'을 저장하는 1차원 배열
# 벡터의 문법은'C()' Combined 의 약자

# 벡터의 특징
# 1. 항상 '동일한 데이터' 타입을 가져야 함
# 2. 만약, 서로다른 타입의 데이터를 포함하면 자동으로 타입변환

# 벡터 생성
vc = c(1, 2, 3, 4) #숫자형 데이터 벡터 생성
print(vc) #벡터 결과 확인

vc = c(1, 2, 3, '4') #마지막 숫자를 문자형태로 삽입된 경우
print(vc) # 전체값이 문자형태로 자동 변경됨
# 숫자형보다 문자형을 더 우선함
# 숫자형(하위) < 문자형(상위) : 자동으로 상위 타입으로 변환

# 벡터의 결과는 세로로 저장되는 형태 (ex 엑셀상 세로(열)로 데이터 추가 하듯)
# c()는 가로안에 들어(삽입)오는 값들을 묶어 하나의 벡터로 만드는 기능
