### 조건문과 반복문은 앞으로 계속 나옴 (day-1 가볍게 알고 가자)

### 반복문
# 특정 작업을 반복적으로 수행할 때 사용
for(i in 1 : 10){ # 1부터 10까지 총 10회 반복 
  print(i)
}

# 벡터랑 for문 사용방법

numbers = c(3, 8, 12, 5, 7, 10, 15) #벡터생성
even_numbers = c() # 빈 벡터 생성


for (i in numbers){   # nubers에 있는 수 만큼 반복(7회 반복)
  if (i %% 2 == 0){   # %% : 나머지값
    even_numbers = c(even_numbers, i)
  }
}

print(even_numbers)



